def calculate_mid_mark():
    sub_1 = float(input("Enter the first mid number: "))
    sub_2 = float(input("Enter the second mid number: "))
    mid_3 = float(input("Enter the third mid number: "))
    total_mid_mark = sub_1 + sub_2 + mid_3
    average_mid_mark = total_mid_mark / 3
    print("The average mid mark is: ", average_mid_mark)
calculate_mid_mark()